/**
 * 
 * ╔═══════════════════════════════════════════════════════════════════════╗
 * ║                           什么是 nano-claw？                          ║
 * ╠═══════════════════════════════════════════════════════════════════════╣
 * ║                                                                        ║
 * ║  🦞 nano-claw 是一个"超轻量级的个人 AI 助手框架"                      ║
 * ║                                                                        ║
 * ║  听起来有点复杂？让我解释一下：                                        ║
 * ║                                                                        ║
 * ║  你可以把 nano-claw 想象成一个"AI 机器人"的"身体"                    ║
 * ║  它可以：                                                              ║
 * ║                                                                        ║
 * ║  ✅ 接收你的消息（通过 Telegram、QQ、Discord 等）                    ║
 * ║  ✅ 理解你的问题（调用 ChatGPT、Claude 等 AI）                       ║
 * ║  ✅ 执行具体任务（写文件、运行命令等）                                ║
 * ║  ✅ 记住对话历史（下次还能接着聊）                                    ║
 * ║  ✅ 扩展技能（通过技能文件添加新能力）                                ║
 * ║                                                                        ║
 * ║  就像钢铁侠的 JARVIS 一样！不过更简单更轻量~                          ║
 * ║                                                                        ║
 * ║  为什么叫"nano"（纳米）？                                            ║
 * ║  → 因为它代码量很少，只有约 4500 行核心代码                           ║
 * ║  → 相比其他框架，更简单、更容易学习和修改                             ║
 * ║                                                                        ║
 * ╚═══════════════════════════════════════════════════════════════════════╝
 * 
 */

