/**
 * 
 * ╔═══════════════════════════════════════════════════════════════════════╗
 * ║                      具体例子：用户让 AI 写文件                         ║
 * ╠═══════════════════════════════════════════════════════════════════════╣
 * ║                                                                        ║
 * ║  场景：用户说"帮我写一个 hello.js 文件"                                ║
 * ║                                                                        ║
 * ║  ─────────────────────────────────────────────────────────────────    ║
 * ║                                                                        ║
 * ║  👤 用户 → "帮我写一个 hello.js 文件"                                 ║
 * ║       │                                                               ║
 * ║       ▼                                                               ║
 * ║  📱 Telegram/QQ/Discord                                              ║
 * ║       │                                                               ║
 * ║       ▼                                                               ║
 * ║  🧠 AgentLoop（大脑）                                                 ║
 * ║       │                                                               ║
 * ║       ├── 📝 Memory: 记录"用户要写文件"                               ║
 * ║       │                                                               ║
 * ║       ├── 📖 Skills: 看看有什么技能能用                              ║
 * ║       │                                                               ║
 * ║       ├── 🔧 Tools: 看看有什么工具（写文件）                          ║
 * ║       │                                                               ║
 * ║       ├── 📋 Context: 整理成完整提示                                  ║
 * ║       │                                                               ║
 * ║       ├── 🧑‍🍳 Provider: 调用 AI (ChatGPT/Claude)                   ║
 * ║       │                                                               ║
 * ║       │   AI 说："我需要调用写文件工具"                               ║
 * ║       │                                                               ║
 * ║       ├── ⚙️ 执行工具: WriteFileTool                                  ║
 * ║       │   → 创建 hello.js 文件                                       ║
 * ║       │   → 写入 console.log("Hello World")                         ║
 * ║       │                                                               ║
 * ║       └── 🎉 返回结果: "好的，文件已创建..."                         ║
 * ║                                                                        ║
 * ║  ─────────────────────────────────────────────────────────────────    ║
 * ║                                                                        ║
 * ║  就像你走进一家餐厅：                                                  ║
 * ║                                                                        ║
 * ║  👤 你："服务员，我想点一份蛋炒饭"                                    ║
 * ║                                                                        ║
 * ║  👨‍🍳 服务员：记下来，交给厨房                                        ║
 * ║                                                                        ║
 * ║  🧑‍🍳 厨房：                                                           ║
 * ║    1. 看看有什么食材（Skills）                                       ║
 * ║    2. 看看有什么厨具（Tools）                                        ║
 * ║    3. 记录顾客点餐（Memory）                                         ║
 * ║    4. 整理菜单（Context）                                            ║
 * ║    5. 去市场买鸡蛋和大米（Provider - 调用 AI）                       ║
 * ║    6. 发现需要炒锅（执行工具 - 写文件）                              ║
 * ║    7. 炒好出锅（返回结果）                                           ║
 * ║                                                                        ║
 * ║  👨‍🍳 服务员：端给你 "您的蛋炒饭好了！"                               ║
 * ║                                                                        ║
 * ╚═══════════════════════════════════════════════════════════════════════╝
 * 
 */

